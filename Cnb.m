function [n2b] = Cnb(q)
n2b(1,1)=q(1)^2+q(2)^2-q(3)^2-q(4)^2;
n2b(1,2)=2*(q(2)*q(3)+q(1)*q(4));
n2b(1,3)=2*(q(2)*q(4)-q(1)*q(3));

n2b(2,1)=2*(q(2)*q(3)-q(1)*q(4));
n2b(2,2)=q(1)^2-q(2)^2+q(3)^2-q(4)^2;
n2b(2,3)=2*(q(3)*q(4)+q(1)*q(2));

n2b(3,1)=2*(q(2)*q(4)+q(1)*q(3));
n2b(3,2)=2*(q(3)*q(4)-q(1)*q(2));
n2b(3,3)=q(1)^2-q(2)^2-q(3)^2+q(4)^2;
end